describe('unknown', () => {
  const pages = [];

  async function createPage() {
    const page = await global.__BROWSER__.newPage();
    await page.setViewport(global.__VIEWPORT__);
    await page.goto('http://localhost:9002/components/unknown/__tests__/unknown.html');
    pages.push(page);
    return page;
  }

  afterAll(async() => {
    for (let i = 0, l = pages.length; i < l; i++) {
      await pages[i].close();
    }
  });

  it('not render anything', async() => {
    const page = await createPage();

    expect(await page.$eval('#unknown', el => el.innerText)).toEqual('');
  });
});
